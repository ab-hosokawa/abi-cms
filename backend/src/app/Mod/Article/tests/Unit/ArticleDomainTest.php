<?php
namespace App\Mod\Article\Tests\Unit;

use App\Mod\Article\Domain\ArticleService;
use App\Mod\Article\Domain\Models\Article;
use Symfony\Component\HttpFoundation\Request;
use Tests\Unit\AbstractUnitTest;

class ArticleDomainTest extends AbstractUnitTest
{
    /** @var ArticleService $service */
    protected $service;

    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub
        $this->service = \app()->make(ArticleService::class);
    }

    public function test_list()
    {
        // データ7件作成
        Article::factory(7)->create();

        // 1ページ目テスト
        $request = new Request();
        $request->query->add([
            'current' => 1,
            'limit' => 5
        ]);
        $result = $this->service->findList($request);
        $this->assertEquals($result['total'], 7);
        $this->assertEquals(count($result['data']), 5);

        // 2ページ目テスト
        $request = new Request();
        $request->query->add([
            'current' => 2,
            'limit' => 5
        ]);
        $result = $this->service->findList($request);
        $this->assertEquals($result['total'], 7);
        $this->assertEquals(count($result['data']), 2);
    }

    public function test_detail()
    {
        // データ作成
        $createPost = Article::factory()->create();
        // データ取得
        $getPost = $this->service->findDetail($createPost->id);
        // 同じデータかテスト
        $this->assertEquals($createPost->title, $getPost->title);
    }

    public function test_save()
    {
        // データ作成テスト
        $inputData = Article::factory()->make()->toArray();
        $request = new Request();
        $request->request->add($inputData);
        $result = $this->service->save($request);
        $post = Article::find($result->id);
        $this->assertEquals($inputData['title'], $post->title);

        // データ更新テスト
        $inputData['title'] .= ' edit';
        $request->request->add($inputData);
        $this->service->save($request, $result->id);
        $post = Article::find($result->id);
        $this->assertEquals($inputData['title'], $post->title);
    }

    public function test_delete()
    {
        $createPost = Article::factory()->create();
        $this->assertEquals(Article::count(), 1);

        $this->service->delete($createPost->id);
        $this->assertEquals(Article::count(), 0);
    }
}
